## 평가를 문자(A, B, C, D, ...)를 입력 받아 내용을 다르게 출력 해보자.

grade = input()

if grade == 'A' or grade == 'a' :
    print("best!!!")
elif grade == 'B' or grade == 'b' :
    print("good!!!")
elif grade == 'C' or grade == 'c' :
    print("run!!!")
elif grade == 'D' or grade == 'd' :
    print("slowly")
else :
    print("what?????????????")
